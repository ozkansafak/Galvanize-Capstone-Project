def note_extractor(m):
	"""
	INPUT: m, match object
	OUTPUT: (tick INT, data (INT, INT))
	"""
	
	tick_p = m.group(1)
	tick = int(re.findall(r'\d+', tick_p)[0])
	data_p = m.group(3)
	data = [int(elem) for elem in  re.findall(r'\D*(\d*)\D+', data_p)]
	
	return tick, data
	
